    private void BTN_salvarCorretorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_salvarCorretorActionPerformed
        if (validarFormulario()) {

            if (flag == 1) {
                corretor.setCreci(TXT_creci.getText().trim());
                corretor.setNome(TXT_nome.getText().trim());
                corretor.setTelefone(TXT_telefone.getText().trim());
                corretor.setCidade(TXT_cidade.getText().trim());
                corretor.setEmail(TXT_email.getText().trim());

                corretorD.cadastrar(corretor);
                carrega();
                JOptionPane.showMessageDialog(null, "Corretor Inserido Com  Sucesso ");
                habilitarFormulario(false);
            } else {
                if (flag == 2) {
                    corretor.setCodigo_Corretor((int) TBL_tabelaCorretor.getValueAt(TBL_tabelaCorretor.getSelectedRow(), 0));
                    corretor.setCreci(TXT_creci.getText());
                    corretor.setNome(TXT_nome.getText());
                    corretor.setTelefone(TXT_telefone.getText());
                    corretor.setCidade(TXT_cidade.getText());
                    corretor.setEmail(TXT_email.getText());
                    corretorD.alterar(corretor);
                    JOptionPane.showMessageDialog(null, "Corretor Alterado Com  Sucesso ");
                    carrega();
                    habilitarFormulario(false);
                }
//
            }

        }


    }//GEN-LAST:event_BTN_salvarCorretorActionPerformed

