    private void BTN_excluirCorretorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_excluirCorretorActionPerformed
        if (TBL_tabelaCorretor.getSelectedRow() != -1) {
            int confirma = JOptionPane.showConfirmDialog(this, "VC DESEJA REALMENTE EXCLUIR CORRETOR DO SEU SISTEMA ", "EXCLUIR CORRETOR", JOptionPane.YES_NO_OPTION);
            if (confirma == JOptionPane.YES_OPTION) {
                corretor.setCodigo_Corretor((int) TBL_tabelaCorretor.getValueAt(TBL_tabelaCorretor.getSelectedRow(), 0));
                corretorD.delete(corretor);
                carrega();
                habilitarFormulario(false);
                limpaFormulario();

            }

        }

    }//GEN-LAST:event_BTN_excluirCorretorActionPerformed

