    private void habilitarFormulario(boolean ativo) {
        BTN_incluirCorretor.setEnabled(!ativo);
        BTN_salvarCorretor.setEnabled(ativo);
        BTN_excluirCorretor.setEnabled(ativo);

        TXT_nome.setEnabled(ativo);
        TXT_telefone.setEnabled(ativo);
        TXT_email.setEnabled(ativo);
        TXT_creci.setEnabled(ativo);
        TXT_cidade.setEnabled(ativo);
        TBL_tabelaCorretor.setEnabled(!ativo);

        if (!ativo) {
            limpaFormulario();
        }
    }

    private void limpaFormulario() {

        TXT_nome.setText("");
        TXT_telefone.setText("");
        TXT_email.setText("");
        TXT_creci.setText("");
        TXT_cidade.setText("");
    }

    private boolean validarFormulario() {
        if (TXT_nome.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, "Nome inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TXT_nome.requestFocus();
            return false;
        }
        if (TXT_telefone.getText().trim().length() != 14) {
            JOptionPane.showMessageDialog(this, " telefone válido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TXT_telefone.requestFocus();
            return false;
        }
        if (TXT_cidade.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, " cidade inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TXT_cidade.requestFocus();
            return false;
        }
        if (TXT_creci.getText().trim().length() != 5) {
            JOptionPane.showMessageDialog(this, "Creci inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TXT_creci.requestFocus();
            return false;
        }
        if (TXT_email.getText().trim().length() < 2) {
            JOptionPane.showMessageDialog(this, " email inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            TXT_email.requestFocus();
            return false;
        }

        return true;

    }

